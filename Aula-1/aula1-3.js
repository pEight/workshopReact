// A sintaxe do for aqui é bem parecia com a de outras linguagens
// Como C por exemplo

// Aqui, ao contrario do exemplo 2, usamos let para definir uma variavel local
// Esta só estará disponível dentro do nosso laço for

for( let foo = 0; foo < 10; foo++){
    console.log(foo);
}

// Mostra os valores de 0 a 9

console.log(foo);
// Erro! 'foo is not defined'