// Essa função é anônima!
// Funções anonimas não possuem nomes, mas podem ser acessadas
// Pelas suas variáveis quando lhes é atribuído uma

let minhaFunc = function (){
    console.log("Oi, eu sou uma funcao!");
}

minhaFunc();
// "Oi, eu sou uma funcao!"

// Acessamos nossa função chamando ela através de sua variável minhaFunc